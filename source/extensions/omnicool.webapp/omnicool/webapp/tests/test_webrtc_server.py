"""
Unit tests for omnicool.webapp.webrtc_server.

These tests validate:
  - WebRTCSignalingServer starts and binds to a port
  - POST /webrtc/offer returns a valid SDP answer when given a real WebRTC
    offer generated by aiortc itself
  - GET  /webrtc/status reflects active connection count
  - The server stops cleanly and rejects new requests after stop()
  - MessageHandler receives and responds to data-channel messages

All tests use real aiortc objects (RTCPeerConnection) to create genuine SDP
offers, so the signaling path is exercised end-to-end without mocking the
crypto / ICE layers.
"""

import asyncio
import json
import unittest

import omni.kit.test

# ── aiortc availability guard ────────────────────────────────────────────────
try:
    import aiohttp
    import aiohttp.web as web
    from aiortc import RTCPeerConnection, RTCSessionDescription, RTCDataChannel
    _HAVE_AIORTC = True
except ImportError:
    _HAVE_AIORTC = False

from omnicool.webapp.webrtc_server import (
    WebRTCSignalingServer,
    _import_aiortc,
    _import_aiohttp,
)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _find_free_port() -> int:
    """Pick an OS-assigned free port."""
    import socket
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


async def _echo_handler(msg: str) -> dict:
    """Simple echo handler used by most tests."""
    try:
        data = json.loads(msg)
    except Exception:
        return {"ok": False, "error": "bad json"}
    return {"id": data.get("id"), "ok": True, "payload": {"echo": data}}


async def _make_offer_sdp() -> str:
    """
    Create a real RTCPeerConnection offer SDP using aiortc.
    The offer is used by test_offer_returns_answer to exercise the full
    signaling path through WebRTCSignalingServer.
    """
    pc = RTCPeerConnection()
    # Add a data channel so the offer contains a media section
    pc.createDataChannel("test")
    offer = await pc.createOffer()
    await pc.setLocalDescription(offer)
    sdp = pc.localDescription.sdp
    await pc.close()
    return sdp


