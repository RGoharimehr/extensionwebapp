@unittest.skipUnless(_HAVE_AIORTC, "aiortc / aiohttp not installed")
class TestWebRTCSignalingServer(omni.kit.test.AsyncTestCase):
    """Tests for WebRTCSignalingServer using real aiortc peer connections."""

    def setUp(self):
        self._port = _find_free_port()
        self._server = WebRTCSignalingServer(message_handler=_echo_handler)

    def tearDown(self):
        # Ensure server is stopped even if a test fails mid-way
        loop = asyncio.get_event_loop()
        if self._server:
            loop.run_until_complete(self._server.stop())
        self._server = None

    # ------------------------------------------------------------------
    # Dependency guard helpers
    # ------------------------------------------------------------------

    async def test_import_aiortc_succeeds(self):
        """_import_aiortc() must not raise when aiortc is installed."""
        self.assertTrue(_import_aiortc())

    async def test_import_aiohttp_succeeds(self):
        """_import_aiohttp() must not raise when aiohttp is installed."""
        self.assertTrue(_import_aiohttp())

    # ------------------------------------------------------------------
    # Server lifecycle
    # ------------------------------------------------------------------

    async def test_start_and_stop(self):
        """Server must start, bind to a port, and stop cleanly."""
        await self._server.start(host="127.0.0.1", port=self._port)
        self.assertEqual(self._server.active_connection_count, 0)
        self.assertEqual(
            self._server.base_url, f"http://127.0.0.1:{self._port}"
        )
        await self._server.stop()

    async def test_status_endpoint_returns_zero_connections(self):
        """GET /webrtc/status must return {"active_connections": 0} when idle."""
        await self._server.start(host="127.0.0.1", port=self._port)
        async with aiohttp.ClientSession() as session:
            async with session.get(
                f"http://127.0.0.1:{self._port}/webrtc/status"
            ) as resp:
                self.assertEqual(resp.status, 200)
                body = await resp.json()
                self.assertEqual(body["active_connections"], 0)
        await self._server.stop()

    async def test_offer_returns_valid_json_answer(self):
        """
        POST /webrtc/offer with a valid SDP offer must return JSON with
        'sdp' and 'type' == 'answer'.
        """
        await self._server.start(host="127.0.0.1", port=self._port)
        offer_sdp = await _make_offer_sdp()

        async with aiohttp.ClientSession() as session:
            async with session.post(
                f"http://127.0.0.1:{self._port}/webrtc/offer",
                json={"sdp": offer_sdp, "type": "offer"},
            ) as resp:
                self.assertEqual(resp.status, 200)
                body = await resp.json()
                self.assertIn("sdp", body)
                self.assertEqual(body["type"], "answer")
                self.assertIsInstance(body["sdp"], str)
                self.assertGreater(len(body["sdp"]), 50)

        await self._server.stop()

    async def test_offer_cors_header_present(self):
        """The /webrtc/offer response must include Access-Control-Allow-Origin."""
        await self._server.start(host="127.0.0.1", port=self._port)
        offer_sdp = await _make_offer_sdp()

        async with aiohttp.ClientSession() as session:
            async with session.post(
                f"http://127.0.0.1:{self._port}/webrtc/offer",
                json={"sdp": offer_sdp, "type": "offer"},
            ) as resp:
                self.assertEqual(
                    resp.headers.get("Access-Control-Allow-Origin"), "*"
                )

        await self._server.stop()

    async def test_offer_missing_sdp_returns_400(self):
        """POST /webrtc/offer without 'sdp' must return HTTP 400."""
